# Prompt:Write a program that converts Celsius temperatures to Fahrenheit temperatures.
# The formula is as follows: F = (9/5)C + 32
# The program should ask the user to enter a temperature in Celsius,
# then display the temperature converted to Fahrenheit.

# I Ask the user to enter their chosen temperature in Celsius for conversion to Fahrenheit
Celsius = float(input("You must enter a temperature in Celsius: "))
print("")

# Convert the temperature to Fahrenheit
Fahrenheit = (9 / 5) * Celsius + 32

# Display the temperature in Fahrenheit
print("The given Celsius temperature,", Celsius, "C in Fahrenheit is:", Fahrenheit, "F")
print("")
